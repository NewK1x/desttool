    private static void clearConsole() {
        try {
            final String os = System.getProperty("os.name");

            // Очистка консоли в зависимости от операционной системы
            if (os.contains("Windows")) {
                new ProcessBuilder("cmd", "/c", "cls").inheritIO().start().waitFor();
            } else {
                System.out.print("\033[H\033[2J");
                System.out.flush();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private static void updateProgressBar(int current, int total) {
        int progress = (int) ((double) current / total * 100);
        System.out.print("\r[\u001B[38;2;255;0;0m*\u001B[0m]Обработка \u001B[38;2;23;171;0m" + progress + "%\u001B");
        System.out.flush();
    }

     private static String getDots(int count) {
        StringBuilder dots = new StringBuilder();
        for (int i = 0; i < count % 4; i++) {
            dots.append(".");
        }
        return dots.toString();
    }

    
